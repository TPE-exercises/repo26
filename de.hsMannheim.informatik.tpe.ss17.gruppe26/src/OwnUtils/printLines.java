package OwnUtils;

public class printLines {
	/**
	 * Zeichnet eine Linie aus (_)
	 */
	public static void printSeperatorLine() {
		System.out.println("___________________________________________________");
	}

	/**
	 * Zeichnet eine Linie aus (.)
	 */
	public static void printSeperatorDots() {
		System.out.println("...................................................");
	}

	/**
	 * Zeichnet eine Linie aus (-)
	 */
	public static void printSeperatorShortLines() {
		System.out.println("---------------------------------------------------");
	}
}
